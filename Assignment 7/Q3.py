num_variable = 42
print("Number:", num_variable, "Type:", type(num_variable))


list_variable = [1, 2, 3, 4, 5]
print("List:", list_variable, "Type:", type(list_variable))


tuple_variable = (10, 20, 30)
print("Tuple:", tuple_variable, "Type:", type(tuple_variable))


byte_variable = b'hello'
print("Byte:", byte_variable, "Type:", type(byte_variable))


bytearray_variable = bytearray(b'world')
print("Bytearray:", bytearray_variable, "Type:", type(bytearray_variable))


set_variable = {1, 2, 3}
print("Set:", set_variable, "Type:", type(set_variable))


frozenset_variable = frozenset({4, 5, 6})
print("Frozenset:", frozenset_variable, "Type:", type(frozenset_variable))


dictionary_variable = {"name": "John", "age": 30}
print("Dictionary:", dictionary_variable, "Type:", type(dictionary_variable))
